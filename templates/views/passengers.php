<h2>About</h2>
<p>Auyuittuq National Park ("the land that never melts") is a national park located on Baffin Island's Cumberland Peninsula,
 Qikiqtaaluk Region in Nunavut, the largest political subdivision of Canada. It features many terrains of Arctic wilderness, such as fjords, glaciers, and ice fields.
  Although Auyuittuq was established in 1976 as a national park reserve, it was upgraded to a full national park in 2000.</p>
<br>
<p>Little vegetation can be found in Auyuittuq Park, although the plants found there range from flowers such as mountain avens, campion,
 Papaver, and saxifrage to shrubs like dwarf birch, Arctic willow, and heather. Many of the plants in Auyuittuq Park grow in clumps to create their own warmer "microclimate" to survive the harsh Arctic conditions.</p>
 <br>
<p>Because of the exceptionally low vegetation supply, wildlife is very scarce. Species that live in Auyuittuq Park include lemmings 
(both the North American brown lemming and the northern collared lemming), red foxes, snowy owls, peregrine falcons, ermines, rough-legged hawks, gyrfalcons, beluga whales, 
snow geese, polar bears, wolves, narwhals, Canada geese, Arctic foxes, Arctic hares, and some barren-ground caribou.</p>
<br>
<p>The most common backpacking route in the park is known as Akshayuk Pass, and follows the Weasel and Owl rivers via Summit Lake.
 In 2008, heavy rain and warm weather caused Summit Lake to burst through its banks, flooding the Weasel River and washing away the Windy Lake bridge. As a result, the hiking routes in the pass are limited to either side of the Weasel River.</p>
